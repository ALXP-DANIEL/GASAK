export const products = createTable("products", {
  id: uuid("id")
    .primaryKey()
    .$defaultFn(() => generateId()),
  name: text("name").notNull(),
  category: productCategoryEnum("category").notNull(),
  description: text("description"),
  // stored in sen (RM cents) to avoid floating point issues
  priceSen: integer("price_sen").notNull(),
  stock: integer("stock").notNull().default(0),
  imageUrl: text("image_url"),
  active: boolean("active").notNull().default(true),
  createdBy: text("created_by").references(() => user.id, {
    onDelete: "set null",
  }),
  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
});

export const orders = createTable(
  "orders",
  {
    id: uuid("id")
      .primaryKey()
      .$defaultFn(() => generateId()),
    orderNo: text("order_no").notNull().unique(),
    customerName: text("customer_name").notNull(),
    customerPhone: text("customer_phone").notNull(),
    customerEmail: text("customer_email").notNull(),
    productId: uuid("product_id")
      .notNull()
      .references(() => products.id, { onDelete: "restrict" }),
    quantity: integer("quantity").notNull().default(1),
    unitPriceSen: integer("unit_price_sen").notNull(),
    totalSen: integer("total_sen").notNull(),
    status: orderStatusEnum("status").notNull().default("pending"),
    paymentMethod: paymentMethodEnum("payment_method"),
    paymentProofUrl: text("payment_proof_url"),
    billplzBillId: text("billplz_bill_id"),
    paymentVerifiedBy: text("payment_verified_by").references(() => user.id, {
      onDelete: "set null",
    }),
    paymentVerifiedAt: timestamp("payment_verified_at", {
      withTimezone: true,
    }),
    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
    updatedAt: timestamp("updated_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [index("gasak_orders_product_idx").on(t.productId)],
);

// ---------------------------------------------------------------------------
// Relations
// ---------------------------------------------------------------------------

export const userRelations = relations(user, ({ one, many }) => ({
  sessions: many(session),
  accounts: many(account),
  profile: one(playerProfiles, {
    fields: [user.id],
    references: [playerProfiles.userId],
  }),
  memberships: many(squadMembers),
}));

export const sessionRelations = relations(session, ({ one }) => ({
  user: one(user, { fields: [session.userId], references: [user.id] }),
}));

export const accountRelations = relations(account, ({ one }) => ({
  user: one(user, { fields: [account.userId], references: [user.id] }),
}));

export const playerProfileRelations = relations(playerProfiles, ({ one }) => ({
  user: one(user, { fields: [playerProfiles.userId], references: [user.id] }),
}));

export const squadRelations = relations(squads, ({ many }) => ({
  members: many(squadMembers),
  events: many(events),
  scrims: many(scrims),
  tournaments: many(tournaments),
  announcements: many(announcements),
}));

export const squadMemberRelations = relations(squadMembers, ({ one }) => ({
  squad: one(squads, {
    fields: [squadMembers.squadId],
    references: [squads.id],
  }),
  user: one(user, { fields: [squadMembers.userId], references: [user.id] }),
}));

export const applicationRelations = relations(applications, ({ one }) => ({
  assignedLeader: one(user, {
    fields: [applications.assignedLeaderId],
    references: [user.id],
  }),
}));

export const eventRelations = relations(events, ({ one }) => ({
  squad: one(squads, { fields: [events.squadId], references: [squads.id] }),
  creator: one(user, { fields: [events.createdBy], references: [user.id] }),
}));

export const tournamentRelations = relations(tournaments, ({ one }) => ({
  squad: one(squads, {
    fields: [tournaments.squadId],
    references: [squads.id],
  }),
}));

export const scrimRelations = relations(scrims, ({ one }) => ({
  squad: one(squads, { fields: [scrims.squadId], references: [squads.id] }),
}));

export const announcementRelations = relations(
  announcements,
  ({ one, many }) => ({
    squad: one(squads, {
      fields: [announcements.squadId],
      references: [squads.id],
    }),
    author: one(user, {
      fields: [announcements.authorId],
      references: [user.id],
    }),
    reads: many(announcementReads),
  }),
);

export const announcementReadRelations = relations(
  announcementReads,
  ({ one }) => ({
    announcement: one(announcements, {
      fields: [announcementReads.announcementId],
      references: [announcements.id],
    }),
    user: one(user, {
      fields: [announcementReads.userId],
      references: [user.id],
    }),
  }),
);

export const productRelations = relations(products, ({ many }) => ({
  orders: many(orders),
}));

export const orderRelations = relations(orders, ({ one }) => ({
  product: one(products, {
    fields: [orders.productId],
    references: [products.id],
  }),
  verifier: one(user, {
    fields: [orders.paymentVerifiedBy],
    references: [user.id],
  }),
}));

export const activityLogRelations = relations(activityLogs, ({ one }) => ({
  actor: one(user, { fields: [activityLogs.actorId], references: [user.id] }),
}));

