export const playerProfiles = createTable("player_profiles", {
  userId: text("user_id")
    .primaryKey()
    .references(() => user.id, { onDelete: "cascade" }),
  fullName: text("full_name"),
  nickname: text("nickname"),
  ign: text("ign"),
  mlbbId: text("mlbb_id"),
  serverId: text("server_id"),
  phone: text("phone"),
  preferredLane: laneEnum("preferred_lane"),
  currentRank: text("current_rank"),
  peakRank: text("peak_rank"),
  updatedAt: timestamp("updated_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
});

export const squads = createTable("squads", {
  id: uuid("id")
    .primaryKey()
    .$defaultFn(() => generateId()),
  name: text("name").notNull(),
  description: text("description"),
  logoUrl: text("logo_url"),
  bannerUrl: text("banner_url"),
  // Optional hex accent color (e.g. #d97b16); falls back to the app-wide gold
  accentColor: text("accent_color"),
  archived: boolean("archived").notNull().default(false),
  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
});

export const squadMembers = createTable(
  "squad_members",
  {
    id: uuid("id")
      .primaryKey()
      .$defaultFn(() => generateId()),
    squadId: uuid("squad_id")
      .notNull()
      .references(() => squads.id, { onDelete: "cascade" }),
    userId: text("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    squadRole: squadRoleEnum("squad_role").notNull().default("player"),
    joinedAt: timestamp("joined_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [
    uniqueIndex("gasak_squad_members_squad_user_idx").on(t.squadId, t.userId),
  ],
);

export const applications = createTable("applications", {
  id: uuid("id")
    .primaryKey()
    .$defaultFn(() => generateId()),
  fullName: text("full_name").notNull(),
  email: text("email").notNull(),
  phone: text("phone").notNull(),
  ign: text("ign").notNull(),
  mlbbId: text("mlbb_id").notNull(),
  serverId: text("server_id").notNull(),
  currentRank: text("current_rank").notNull(),
  preferredLane: laneEnum("preferred_lane").notNull(),
  heroPool: text("hero_pool").notNull(),
  previousTeam: text("previous_team"),
  introduction: text("introduction").notNull(),
  status: applicationStatusEnum("status").notNull().default("applied"),
  assignedLeaderId: text("assigned_leader_id").references(() => user.id, {
    onDelete: "set null",
  }),
  reviewNotes: text("review_notes"),
  createdAt: timestamp("created_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
  updatedAt: timestamp("updated_at", { withTimezone: true })
    .notNull()
    .defaultNow(),
});

export const events = createTable(
  "events",
  {
    id: uuid("id")
      .primaryKey()
      .$defaultFn(() => generateId()),
    title: text("title").notNull(),
    description: text("description"),
    type: eventTypeEnum("type").notNull().default("practice"),
    startsAt: timestamp("starts_at", { withTimezone: true }).notNull(),
    endsAt: timestamp("ends_at", { withTimezone: true }),
    location: text("location"),
    // null squadId means an org-wide event
    squadId: uuid("squad_id").references(() => squads.id, {
      onDelete: "cascade",
    }),
    createdBy: text("created_by").references(() => user.id, {
      onDelete: "set null",
    }),
    createdAt: timestamp("created_at", { withTimezone: true })
      .notNull()
      .defaultNow(),
  },
  (t) => [index("gasak_events_squad_idx").on(t.squadId)],
);

